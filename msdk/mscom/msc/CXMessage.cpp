/////////////////////////////////////////////////////////////////////////////
BYTE CXMessage::VersionMajor()
{
	return byVersionMajor;
}

/////////////////////////////////////////////////////////////////////////////
BYTE CXMessage::VersionMinor()
{
	return byVersionMinor;
}

/////////////////////////////////////////////////////////////////////////////
RSMSGID CXMessage::MessageID()
{
	return dwMessageID;
}

/////////////////////////////////////////////////////////////////////////////
BOOL CXMessage::IsNotify()
{
	return dwType == XMTYPE_NOTIFY;
}

/////////////////////////////////////////////////////////////////////////////
BOOL CXMessage::IsRequest()
{
	return dwType == XMTYPE_REQUEST;
}

/////////////////////////////////////////////////////////////////////////////
BOOL CXMessage::IsResponse()
{
	return dwType == XMTYPE_RESPONSE;
}

/////////////////////////////////////////////////////////////////////////////
DWORD CXMessage::UniqueID()
{
	return dwUniqueID;
}

/////////////////////////////////////////////////////////////////////////////
DWORD CXMessage::DataLen()
{
	return dwDataLen;
}

/////////////////////////////////////////////////////////////////////////////
LPBYTE CXMessage::Data()
{
	return (LPBYTE)(this + 1);
}

/////////////////////////////////////////////////////////////////////////////
VOID CXMessage::Initialize(RSMSGID dwMessageID, DWORD dwDataLen, DWORD dwUniqueID)
{
	byVersionMajor    = 1;
	byVersionMinor    = 0;
	wReserve          = 0;
	this->dwMessageID = dwMessageID;
	this->dwUniqueID  = dwUniqueID;
	dwInnerUseHight   = 0;
	dwInnerUseLow     = 0;
	this->dwDataLen   = dwDataLen;
}